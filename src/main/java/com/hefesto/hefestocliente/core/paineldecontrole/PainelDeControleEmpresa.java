    @Override
    public void Limpar() {
        super.Limpar();

        empresaInfo = new HefEmpresaInfo();
        log = null;
        carregar(new BeanEvent(this, null));
    }

    @Override
    public void salvar() {
        if (!empresaCNPJ.getValorString().isEmpty()) {
            if (!HFValidator.validaCNPJ(empresaCNPJ.getValorString())) {
                HFBallonUtil.showTimedBallon(empresaCNPJ, Messages.getMessage("empresa.cnpj.erro"), MessageIcon.ERROR.getIcon());
                return;
            }
        }
        if (empresaDesc.Valida()
                && empresaCod.Valida()) {
            HefEmpresa empresa;
            if (empresaInfo.getIdempresa() == null) {
                empresa = new HefEmpresa();
            } else {
                empresa = empresaInfo.getIdempresa();
            }
            empresa.setDescricao(empresaDesc.getText());
            empresaInfo.setIdempresa(empresa);
            empresaInfo.setBairro(empresaBairro.getText());
            empresaInfo.setCnpj(empresaCNPJ.getValorBigint());
            empresaInfo.setCodigo(empresaCod.getText());
            empresaInfo.setComplemento(empresaComplemento.getText());
            empresaInfo.setEmail(empresaEmail.getText());
            empresaInfo.setIdcancelada(!idFilialCancelada.isSelected());
            empresaInfo.setNumero(empresaNumero.getBigIntValor());
            empresaInfo.setRua(empresaRua.getText());
            empresaInfo.setTelefone(empresaTelefone.getValorBigint());
            empresaInfo.setIdlogradouro(log);
            salvar(new BeanEvent(this, empresaInfo));
        }

    }

    private void montaFilial(HefEmpresaInfo empresaInfo) {

        this.empresaInfo = empresaInfo;
        empresaDesc.setText(empresaInfo.getIdempresa().getDescricao());
        empresaCod.setText(empresaInfo.getCodigo());
        empresaBairro.setText(empresaInfo.getBairro());
        empresaCNPJ.setValorComZerosLPAD(empresaInfo.getCnpj(), 14);
        empresaCep.setValorComZerosLPAD(empresaInfo.getIdlogradouro() != null ? empresaInfo.getIdlogradouro().getCep() + "" : "0", 8);
        empresaComplemento.setText(empresaInfo.getComplemento());
        empresaCidade.setText(empresaInfo.getIdlogradouro() != null ? empresaInfo.getIdlogradouro().getIdcidade().getDescricao() : "");
        empresaEmail.setText(empresaInfo.getEmail());
        empresaNumero.setText(empresaInfo.getNumero() != null ? empresaInfo.getNumero() + "" : "");
        empresaRua.setText(empresaInfo.getRua());
        empresaTelefone.setValorComZerosRPAD(empresaInfo.getTelefone(), 12);
        idFilialCancelada.setSelected(!empresaInfo.getIdcancelada());
        log = empresaInfo.getIdlogradouro();

    }

    public void montaTabelaEmpresa(List<?> lst) {
        listaEmpresas.removeTable();
        listaEmpresas.addListRows(lst);
    }

    @Override
    public void update(java.util.Observable o, Object arg) {
        if (arg instanceof HefLogradouro) {
            log = (HefLogradouro) arg;
            empresaCidade.setText(log.getIdcidade().getSigla() + " - " + log.getDescricao());
            empresaCep.setValorComZerosLPAD(log.getCep(), 8);
        }
    }


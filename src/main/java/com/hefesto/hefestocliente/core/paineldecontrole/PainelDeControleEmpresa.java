    private void empresaCepFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_empresaCepFocusLost
        log = listner.buscaLogradouroCep(new BeanEvent(this, empresaCep.getValorBigint()));
        if (log != null) {
            empresaCidade.setText(log.getIdcidade().getSigla() + " - " + log.getDescricao());
            empresaCep.setValorComZerosLPAD(log.getCep(), 8);
        } else {
            empresaCidade.setText("");
            empresaCep.limpar();
        }
    }//GEN-LAST:event_empresaCepFocusLost


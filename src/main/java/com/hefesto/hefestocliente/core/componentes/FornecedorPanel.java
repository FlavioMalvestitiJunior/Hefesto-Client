    public HefFornecedorInfo getFornecedor() {
        if (fornecedor.getIdfornecedor() != null) {
            fornecedor.setIdfornecedor(fornecedor.getIdfornecedor());
        } else if (fornecedor.getIdfornecedor() == null && editavel) {
            fornecedor.setIdfornecedor(new HefFornecedor());
            fornecedor.getIdfornecedor().setCodigoforncedor(codFornecedor.getText());
        } else {
            frame.throwError(new Exception(Messages.getMessage("fornecedor.erro.invalido")));
            return null;
        }

        fornecedor.setNome(razaoFornecedor.getText());
        fornecedor.setNomeFantasia(nomeFantFornecedor.getText());
        fornecedor.getIdfornecedor().setCpfCnpj(cpfCnpjFornecedor.getValorBigint());
        fornecedor.setIdpessoafisica(idPessoaFisica.isSelected());
        return fornecedor;
    }

    public void limpar() {
        frame.LimpaComponentes(new Component[]{this});
        fornecedor = null;

    }

    public void montaFornecedor(HefFornecedorInfo info) {
        this.fornecedor = info;
        if (info.getIdfornecedorInfo() != null) {
            codFornecedor.setText(info.getIdfornecedor().getCodigoforncedor());
            razaoFornecedor.setText(info.getNome());
            nomeFantFornecedor.setText(info.getNomeFantasia());
            cpfCnpjFornecedor.setValor(info.getIdfornecedor().getCpfCnpj());
            idPessoaFisica.setSelected(info.getIdpessoafisica());
            idPessoaJuridica.setSelected(!info.getIdpessoafisica());
        }
    }


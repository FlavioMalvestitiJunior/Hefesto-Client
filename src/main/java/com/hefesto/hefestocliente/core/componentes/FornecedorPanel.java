    private void codFornecedorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codFornecedorFocusLost
        if (fornecedor == null || !editavel) {
            if (codFornecedor.valida()) {
                fornecedor = bean.buscaFornecedor(new BeanEvent(frame, codFornecedor.getText()));
            } else {
                fornecedor = new HefFornecedorInfo();
            }
            montaFornecedor(fornecedor);
        }
    }//GEN-LAST:event_codFornecedorFocusLost



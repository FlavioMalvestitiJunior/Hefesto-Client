    private void cepFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cepFocusLost
        log = listner.buscaLogradouroCep(new BeanEvent(this, cep.getValorBigint()));
        if (log != null) {
            cidade.setText(log.getIdcidade().getSigla() + " - " + log.getDescricao());
            cep.setValorComZerosLPAD(log.getCep(), 8);
        } else {
            cidade.setText("");
            cep.limpar();
        }
    }//GEN-LAST:event_cepFocusLost


    private void listaFornecedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaFornecedoresMouseClicked
        if (evt.getClickCount() == 2) {
            if (listaFornecedores.getSelectedRowCount() > -1) {
                HefFornecedorInfo info = (HefFornecedorInfo) listaFornecedores.getValorColunaDaLinhaSelecionada(OBJ);
                montaFornecedor(info);
                fornecedorPanel1.montaFornecedor(info);
            }
        }
    }//GEN-LAST:event_listaFornecedoresMouseClicked


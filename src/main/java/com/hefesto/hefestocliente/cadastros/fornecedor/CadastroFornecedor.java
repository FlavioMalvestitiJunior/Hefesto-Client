    @Override
    public void Limpar() {
        super.Limpar();
        fornecedorPanel1.limpar();
        tabPainel.setSelectedComponent(fornecedoresCadastradosPanel);
        carregar(new BeanEvent(this, null));
    }

    @Override
    public void salvar() {
        HefFornecedorInfo info = fornecedorPanel1.getFornecedor();
        if (info != null) {
            if (empresa.valida() && cep.Valida()
                    && rua.Valida() && numero.Valida()
                    && bairro.Valida() && telefone.Valida()) {
                info.setBairro(bairro.getText());
                info.setCelular(celular.getValorBigint());
                info.setComplemento(complemento.getText());
                info.setEmail(email.getText());
                info.setIdempresa((HefEmpresa) empresa.getObjetoSelecionado());
                info.setIdlogradouro(log);
                info.setNumero(numero.getBigIntValor());
                info.setNurg(rg.getValorString());
                info.setRua(rua.getText());
                info.setSite(site.getText());
                info.setTelefone(telefone.getValorBigint());

                salvar(new BeanEvent(this, info));

            }

        }

    }

    private void montaFornecedor(HefFornecedorInfo info) {
        if (info.getIdfornecedorInfo() != null) {
            tabPainel.setSelectedComponent(infCompPainel);
            empresa.setObjetoSelecionado(info.getIdempresa());
            cep.setValor(info.getIdlogradouro().getCep());
            cidade.setText(info.getIdlogradouro().getDescricao() + " - " + info.getIdlogradouro().getIdcidade().getDescricao());
            rua.setText(info.getRua());
            bairro.setText(info.getBairro());
            numero.setText(info.getNumero() + "");
            complemento.setText(info.getComplemento());
            email.setText(info.getEmail());
            rg.setValor(info.getNurg());
            site.setText(info.getSite());
            telefone.setValor(info.getTelefone());
            celular.setValor(info.getCelular());
            log = info.getIdlogradouro();
        }

    }

    public void montaTabelaFornecedor(List<?> lst) {
        listaFornecedores.removeTable();
        listaFornecedores.addListRows(lst);
    }

    @Override
    public void update(java.util.Observable o, Object arg) {
        if (arg instanceof HefFornecedorInfo) {
            montaFornecedor((HefFornecedorInfo) arg);
        } else if (arg instanceof HefLogradouro) {
            log = (HefLogradouro) arg;
            cidade.setText(log.getIdcidade().getSigla() + " - " + log.getDescricao());
            cep.setValorComZerosLPAD(log.getCep(), 8);
        }
    }

    public void montaComboEmpresa(List<?> montaComboEmpresa) {
        empresa.adicionarItens(montaComboEmpresa);
    }

